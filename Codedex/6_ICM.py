# Nivel de índice de masa corporal (ICM), en inglés (bmi)
  
print("Vamos a sacar tu ICM (índice de masa muscular")
peso = float(input("Por favor indica tu peso en kilogramos: "))
altura = float(input("Indica ahora tu altura en metros: "))

bmi = (peso)/(altura ** 2)

print("Tu nivel de bmi es: ",bmi)
